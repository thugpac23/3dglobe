/** Pick a random fact for the given country, or null if no facts exist. */
export function getFact(iso: string): string | null {
  const list = COUNTRY_FACTS[iso];
  if (!list || !list.length) return null;
  return list[Math.floor(Math.random() * list.length)];
}

/** Pick a random country and a random fact from its list. */
export function getRandomFactWithCountry(): { iso: string; fact: string } | null {
  const isos = Object.keys(COUNTRY_FACTS).filter(k => COUNTRY_FACTS[k].length);
  if (!isos.length) return null;
  const iso  = isos[Math.floor(Math.random() * isos.length)];
  const fact = getFact(iso);
  return fact ? { iso, fact } : null;
}
